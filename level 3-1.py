def getNeighbors(node, map):
  nodes = []

  xpos = node[0]
  ypos = node[1]

  if xpos > 0:
    if map[xpos - 1][ypos] == 0:
      nodes.append((xpos - 1, ypos))

  if xpos < len(map) - 1:
    if map[xpos + 1][ypos] == 0:
      nodes.append((xpos + 1, ypos))

  if ypos > 0:
    if map[xpos][ypos - 1] == 0:
      nodes.append((xpos, ypos - 1))

  if ypos < len(map[0]) - 1:
    if map[xpos][ypos + 1] == 0:
      nodes.append((xpos, ypos + 1))

  return nodes

def solution(map):
  walls = []
  for y in range(len(map[0])):
    for x in range(len(map)):
      if map[x][y] == 1:
        walls.append((x, y))
  solutions = []

  if not walls:
    walls.append((0, 0))

  for x, y in walls:
    map[x][y] = 0

    entry = (0, 0)
    exit = (len(map) - 1, len(map[0]) - 1)
    queue = [(entry, [])]
    visited = []
    uncovered = []
    uncovered = uncovered + getNeighbors(entry, map)

    while (len(queue) > 0):
      node, path = queue.pop(0)
      path.append(node)
      visited.append(node)

      if node == exit:
        solutions.append(len(path))
      
      neighbors = getNeighbors(node, map)
      for n in neighbors:
        if not n in visited:
          if not any(d[0] == n for d in queue):
            queue.append((n, path[:]))   

    map[x][y] = 1  
  
  return min(solutions)


